    private void txtBtnPagosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBtnPagosActionPerformed
        String idPedido = txtIdPedido.getText();
        String idCliente = txtIdCliente.getText();
        String idProducto = txtIdProducto.getText();
        String cant = (String) ComboBoxCant.getSelectedItem();
        String fecha = txtFecha.getText();
        if(idPedido.isEmpty() || idCliente.isEmpty() || idProducto.isEmpty() || fecha.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo o campos vac√≠os");
        } else {
            try {
                Connection con = Conexion.Entrar();
                Statement st = con.createStatement();
                ResultSet rs = st.executeQuery("select Precio from Producto where IdP = '" + idProducto+"'" );
                rs.next();
                precio = rs.getFloat(1);
                txtIdPedido.setEditable(false);
                txtIdCliente.setEditable(false);
                txtIdProducto.setEditable(false);
                txtFecha.setEditable(false);
                ComboBoxCant.setEditable(false);
                ComboBoxCant.setEnabled(false);
                btnGuardar.setEnabled(true);
                txtBtnPagos.setEnabled(false);
            
                int cantProducto = Integer.parseInt(cant);
                float subtotal = precio * (float)cantProducto;
                float IVA = subtotal * (float)0.16;
                float Total = subtotal + IVA;
                txtSub.setText(String.valueOf(subtotal));
                txtIVA.setText(String.valueOf(IVA));
                txtTotal.setText(String.valueOf(Total));
            } catch(SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_txtBtnPagosActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String idPedido = txtIdPedido.getText();
        String idCliente = txtIdCliente.getText();
        String idProducto = txtIdProducto.getText();
        String cant = (String) ComboBoxCant.getSelectedItem();
        String fecha = txtFecha.getText();
        try {
            int cantProducto = Integer.parseInt(cant);
            float subtotal = precio * (float)cantProducto;
            float IVA = precio * (float)0.16;
            float Total = subtotal + IVA;
            Connection con;
            String idEP = idPedido;
            PreparedStatement ps;
            try {
            con = Conexion.Entrar();
            String ingresar = "insert into Encabezado_Pedidos (IdEP,IdCliente,Fecha,Total)values(?,?,?,?)";
            ps =con.prepareStatement(ingresar);
            ps.setString(1,idEP);
            ps.setString(2,idCliente);
            ps.setString(3,fecha);
            ps.setFloat(4,Total);
            ps.executeUpdate();
            ps.close();
            con.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());

            }
            
            
            con = Conexion.Entrar();
            String insertar = "insert into Pedido (IdPedido,IdP,IdEP,Cantidad,Subtotal,IVA)values(?,?,?,?,?,?)";
            ps=con.prepareStatement(insertar);
            ps.setString(1,idPedido);
            ps.setString(2, idProducto);
            ps.setString(3, idEP);
            ps.setInt(4, cantProducto);
            ps.setFloat(5, subtotal);
            ps.setFloat(6, IVA);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Pedido agregado correctamente");
            ps.close();
            con.close();           
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "Error: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

